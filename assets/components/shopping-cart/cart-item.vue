<template>
    <div :class="[$style.component, 'row', 'p-3', 'align-items-center']">
        <div class="col-3">
            <div class="row">
                <span class="col">
                    {{ cartItem.name }}
                </span>

                <span class="col-2">
                    <span
                        class="color-square"
                        :style="{
                            backgroundColor: `#${cartItem.hexColor}`
                        }"
                    />
                </span>
            </div>
        </div>

        <div class="col-3">
            <input
                v-model.number="cartItem.qty"
                class="form-control"
                type="number"
                min="1"
                @input="$emit('updateQuantity', {
                    product: cartItem['@id'],
                    color: cartItem.colorId,
                    quantity: cartItem.qty
                })"
            >
        </div>

        <div class="col-3">
            ${{ totalPrice }}
        </div>

        <div class="col-3">
            <button
                class="btn btn-info btn-sm"
                @click="$emit('removeFromCart', {
                    product: cartItem['@id'],
                    color: cartItem.colorId
                })"
            >
                Remove
            </button>
        </div>
    </div>
</template>

<script>
import formatPrice from '@/helpers/format-price';

export default {
    name: 'ShoppingCartItem',
    props: {
        cartItem: {
            type: Object,
            required: true,
        },
    },
    computed: {
        totalPrice() {
            return formatPrice(this.cartItem.price * this.cartItem.qty);
        },
    },
};
</script>

